#!/usr/bin/env python3
"""Benchmark AmyloGram-Py lookup loading and FASTA-style prediction."""

from __future__ import annotations

import argparse
from pathlib import Path
import sys
from time import perf_counter


PROJECT = Path(__file__).resolve().parents[1]
SRC = PROJECT / "src"
if str(SRC) not in sys.path:
    sys.path.insert(0, str(SRC))

from amylogram_py.lookup import (  # noqa: E402
    read_probability_table,
    read_probability_table_binary,
    write_probability_table_binary,
)
from amylogram_py.encoding import clean_sequence, encode_groups, rolling_sixmer_codes  # noqa: E402
from amylogram_py.predict import predict_clean_sequence_probability, predict_sequence_probability  # noqa: E402


JSON_LOOKUP = PROJECT / "tests" / "fixtures" / "amylogram_sixmer_probabilities.json"
BIN_LOOKUP = PROJECT / "tests" / "fixtures" / "amylogram_sixmer_probabilities.bin"


def synthetic_sequences(count: int, length: int) -> list[str]:
    seed = "ACDEFGHIKLMNPQRSTVWY"
    return [(seed[i % len(seed) :] + seed[: i % len(seed)]) * ((length // len(seed)) + 1) for i in range(count)]


def timed(label: str, fn):
    start = perf_counter()
    result = fn()
    elapsed = perf_counter() - start
    return label, elapsed, result


def write_report(metrics: dict[str, float | int | str], path: Path) -> None:
    lines = [
        "# AmyloGram-Py Lookup Benchmark",
        "",
        "This report was generated by `evidence/benchmark_lookup_prediction.py`.",
        "",
        "## Inputs",
        "",
        f"- JSON lookup: `{JSON_LOOKUP.relative_to(PROJECT)}`",
        f"- Binary lookup: `{BIN_LOOKUP.relative_to(PROJECT)}`",
        f"- Sequences: `{metrics['sequence_count']}`",
        f"- Sequence length: `{metrics['sequence_length']}`",
        "",
        "## Metrics",
        "",
        "| Metric | Value |",
        "|---|---:|",
        f"| JSON lookup size bytes | {metrics['json_size_bytes']} |",
        f"| Binary lookup size bytes | {metrics['bin_size_bytes']} |",
        f"| JSON load seconds | {metrics['json_load_seconds']:.6f} |",
        f"| Binary load seconds | {metrics['bin_load_seconds']:.6f} |",
        f"| Streaming prediction seconds | {metrics['streaming_prediction_seconds']:.6f} |",
        f"| Streaming sequences per second | {metrics['streaming_sequences_per_second']:.2f} |",
        f"| Clean streaming seconds | {metrics['clean_streaming_prediction_seconds']:.6f} |",
        f"| Clean streaming sequences per second | {metrics['clean_streaming_sequences_per_second']:.2f} |",
        f"| Legacy list prediction seconds | {metrics['legacy_prediction_seconds']:.6f} |",
        f"| Legacy sequences per second | {metrics['legacy_sequences_per_second']:.2f} |",
        f"| Speedup vs legacy | {metrics['speedup_vs_legacy']:.2f} |",
        f"| Clean speedup vs legacy | {metrics['clean_speedup_vs_legacy']:.2f} |",
        f"| Streaming checksum | {metrics['streaming_checksum']:.8f} |",
        f"| Clean streaming checksum | {metrics['clean_streaming_checksum']:.8f} |",
        f"| Legacy checksum | {metrics['legacy_checksum']:.8f} |",
        "",
    ]
    path.write_text("\n".join(lines), encoding="utf-8")


def predict_sequence_probability_legacy(sequence: str, table: list[float]) -> float:
    groups = encode_groups(sequence)
    codes = rolling_sixmer_codes(groups)
    if not codes:
        raise ValueError("AmyloGram requires sequences of at least six amino acids")
    return max(table[code] for code in codes)


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Benchmark AmyloGram-Py lookup prediction.")
    parser.add_argument("--count", type=int, default=10000)
    parser.add_argument("--length", type=int, default=300)
    parser.add_argument("--report", default=str(PROJECT / "evidence" / "lookup_benchmark.md"))
    args = parser.parse_args(argv)

    if not JSON_LOOKUP.exists():
        raise SystemExit(f"Missing JSON lookup table: {JSON_LOOKUP}")
    if not BIN_LOOKUP.exists():
        table = read_probability_table(JSON_LOOKUP)
        write_probability_table_binary(table, BIN_LOOKUP)

    _, json_load_seconds, json_table = timed("json_load", lambda: read_probability_table(JSON_LOOKUP))
    _, bin_load_seconds, bin_table = timed("bin_load", lambda: read_probability_table_binary(BIN_LOOKUP))
    if json_table != bin_table:
        raise SystemExit("JSON and binary lookup tables differ")

    sequences = [sequence[: args.length] for sequence in synthetic_sequences(args.count, args.length)]
    clean_sequences = [clean_sequence(sequence) for sequence in sequences]

    start = perf_counter()
    streaming_probabilities = [predict_sequence_probability(sequence, bin_table) for sequence in sequences]
    streaming_prediction_seconds = perf_counter() - start

    start = perf_counter()
    clean_streaming_probabilities = [
        predict_clean_sequence_probability(sequence, bin_table) for sequence in clean_sequences
    ]
    clean_streaming_prediction_seconds = perf_counter() - start

    start = perf_counter()
    legacy_probabilities = [predict_sequence_probability_legacy(sequence, bin_table) for sequence in sequences]
    legacy_prediction_seconds = perf_counter() - start
    if streaming_probabilities != clean_streaming_probabilities or streaming_probabilities != legacy_probabilities:
        raise SystemExit("Prediction paths differ")

    metrics: dict[str, float | int | str] = {
        "sequence_count": args.count,
        "sequence_length": args.length,
        "json_size_bytes": JSON_LOOKUP.stat().st_size,
        "bin_size_bytes": BIN_LOOKUP.stat().st_size,
        "json_load_seconds": json_load_seconds,
        "bin_load_seconds": bin_load_seconds,
        "streaming_prediction_seconds": streaming_prediction_seconds,
        "streaming_sequences_per_second": (
            args.count / streaming_prediction_seconds if streaming_prediction_seconds > 0 else 0.0
        ),
        "clean_streaming_prediction_seconds": clean_streaming_prediction_seconds,
        "clean_streaming_sequences_per_second": (
            args.count / clean_streaming_prediction_seconds if clean_streaming_prediction_seconds > 0 else 0.0
        ),
        "legacy_prediction_seconds": legacy_prediction_seconds,
        "legacy_sequences_per_second": args.count / legacy_prediction_seconds if legacy_prediction_seconds > 0 else 0.0,
        "speedup_vs_legacy": (
            legacy_prediction_seconds / streaming_prediction_seconds if streaming_prediction_seconds > 0 else 0.0
        ),
        "clean_speedup_vs_legacy": (
            legacy_prediction_seconds / clean_streaming_prediction_seconds
            if clean_streaming_prediction_seconds > 0
            else 0.0
        ),
        "streaming_checksum": sum(streaming_probabilities),
        "clean_streaming_checksum": sum(clean_streaming_probabilities),
        "legacy_checksum": sum(legacy_probabilities),
    }

    print("metric\tvalue")
    for key, value in metrics.items():
        print(f"{key}\t{value}")

    write_report(metrics, Path(args.report))
    print(f"Markdown report written: {args.report}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
